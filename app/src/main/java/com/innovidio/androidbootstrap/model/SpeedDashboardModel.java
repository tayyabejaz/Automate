public class SpeedDashboardModel {

    private String title;
    private String speedView;
    private String unitView;
    private String maxSpeedView;
    private String avgSpeedView;
    private String distanceView;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getSpeedView() {
        return speedView;
    }

    public void setSpeedView(String speedView) {
        this.speedView = speedView;
    }

    public String getUnitView() {
        return unitView;
    }

    public void setUnitView(String unitView) {
        this.unitView = unitView;
    }

    public String getMaxSpeedView() {
        return maxSpeedView;
    }

    public void setMaxSpeedView(String maxSpeedView) {
        this.maxSpeedView = maxSpeedView;
    }

    public String getAvgSpeedView() {
        return avgSpeedView;
    }

    public void setAvgSpeedView(String avgSpeedView) {
        this.avgSpeedView = avgSpeedView;
    }

    public String getDistanceView() {
        return distanceView;
    }


    public void setDistanceView(String distanceView) {
        this.distanceView = distanceView;

    }

    public SpeedDashboardModel() {

    }
}
